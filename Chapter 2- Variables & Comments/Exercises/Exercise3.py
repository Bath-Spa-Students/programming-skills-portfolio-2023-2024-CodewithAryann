# Defining the person name
name="\n\t Aryan Wasi \n\t"

#Printing the name once with whitespace
print("Name with whitespace:",name)

#Printing the after applying lstrip() 
print("Name after lstrip():", name.lstrip())

#Printing the name after applying rstrip()
print("Name after rstrip():", name.rstrip())

# Print the name after applying strip() 
print("Name after strip():", name.strip())